const categoryLists: Map<ApiTypeCategory, Array<ApiSyncableType>> = new Map<ApiTypeCategory, Array<ApiSyncableType>>();

export function castorStandardTypeCategoryList(category: ApiTypeCategory): Array<ApiSyncableType> {
    if (!categoryLists.has(category)) {
        categoryLists.set(
            category,
            castor_standard_types.filter((type) => type.type_category_uuid === category)
        );
    }
    return categoryLists.get(category);
}

export function castorStandardTypeByLabel(label: string): ApiSyncableType {
    return castor_standard_types.find((type) => type.type_label === label);
}

export function castorStandardTypeToLabel(uuid: string): string {
    return castor_standard_types.find((type) => type.type_uuid === uuid)?.type_label ?? "";
}

